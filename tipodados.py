

# Tipo inteiro (int)
numero_inteiro = 10
print(f"Tipo de numero_inteiro: {type(numero_inteiro)} - Valor: {numero_inteiro}")

# Tipo ponto flutuante (float)
numero_ponto_flutuante = 10.5
print(f"Tipo de numero_ponto_flutuante: {type(numero_ponto_flutuante)} - Valor: {numero_ponto_flutuante}")

# Tipo string (str)
texto = "Olá, mundo!"
print(f"Tipo de texto: {type(texto)} - Valor: {texto}")

# Tipo booleano (bool)
verdadeiro = True
falso = False
print(f"Tipo de verdadeiro: {type(verdadeiro)} - Valor: {verdadeiro}")
print(f"Tipo de falso: {type(falso)} - Valor: {falso}")

# Tipo lista (list)
lista = [1, 2, 3, 4, 5]
print(f"Tipo de lista: {type(lista)} - Valor: {lista}")

# Tipo tupla (tuple)
tupla = (1, 2, 3, 4, 5)
print(f"Tipo de tupla: {type(tupla)} - Valor: {tupla}")

# Tipo conjunto (set)
conjunto = {1, 2, 3, 4, 5}
print(f"Tipo de conjunto: {type(conjunto)} - Valor: {conjunto}")

# Tipo dicionário (dict)
dicionario = {"nome": "João", "idade": 30}
print(f"Tipo de dicionario: {type(dicionario)} - Valor: {dicionario}")